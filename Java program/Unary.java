public class Unary {

    public static void main(String[] args) {
        
        int x = 5;
        System.out.println("x:"+x);
        x++;
        System.out.println("x:"+x);
        ++x;
        System.out.println("x:"+x);
        x--;
        System.out.println("x:"+x);
        --x;
        System.out.println("x:"+x);
    }
    
}
