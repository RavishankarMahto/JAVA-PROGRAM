public class Comparison_operator {

    public static void main(String[] args) {
        int x= 10;
        int y = 20;

        boolean result1 = x<y;
        boolean result2 = y>x;

        System.out.println(result1+"/t"+result2);
        System.out.println("---------");
        System.out.println(x<=10);
        System.out.println(3>=y);
        System.out.println("------------");
        System.out.println(x==10);
        System.out.println(y==30);
        System.out.println("------------");
        System.out.println(x!=10);
        System.out.println(y!=30);
        
    }
    
}
